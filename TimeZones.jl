# ╔═╡ b99daa08-736c-4083-a2e2-12ed7b6ab553
begin
	zdt_london = ZonedDateTime(year(zdt), month(zdt), day(zdt), hm[1], hm[2], tz"Europe/London")
	names_and_zones = [
		("New York, USA    ", tz"America/New_York"),
		("London, UK       ", tz"Europe/London"),
		("Rome, Italy      ", tz"Europe/Rome"),
		("Kolkata, India   ", tz"Asia/Kolkata"),
		("Bangkok, Thailand", tz"Asia/Bangkok")
	]
	
	foreach(t -> begin
		converted_zdt = astimezone(zdt_london, t[2])
		println(t[1], " = ", @sprintf("%02d", hour(converted_zdt)), ":", @sprintf("%02d", minute(converted_zdt)))
	end, names_and_zones)
end

